//Operator Aritmatika
var a = 10;
console.log('Nilai a : ' + a);
var b = 4;
console.log('Nilai b : ' + b);

console.log(`${a} + ${b} = ${a+b}`);
console.log(`${a} - ${b} = ${a-b}`);
console.log(`${a} * ${b} = ${a*b}`);
console.log(`${a} / ${b} = ${a/b}`);
console.log(`${a} % ${b} = ${a%b}`);
console.log(`${a} ** ${b} = ${a**b}`);